    public ResultSet select() {
        ResultSet rs = null;
        String SQL = "select * from Usuarios";
        try {
            rs = stmt.executeQuery(SQL);
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return rs;
    }
    
    public ResultSet search() {
        ResultSet rs = null;
        String SQL = "select * from usuarios where nome like '%" + jTextFieldPesquisa.getText() + "%'";
        try {
            rs = stmt.executeQuery(SQL);
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return rs;
    }

    public void delete(String id) {
        String SQL = "DELETE from usuarios WHERE id = " + id;
        try {
            this.stmt.executeUpdate(SQL);
        } catch (SQLException ex) {
           ex.printStackTrace();
        }
    }

    public void insert() {
        String SQL = "insert into usuarios(nome, login, senha, perfil) "
                + "values(?,?,md5(?),?)";
        try {
            PreparedStatement ps = this.conexao.getConexao().prepareStatement(SQL);

            ps.setString(1, jTextFieldNome.getText());
            ps.setString(2, jTextFieldLogin.getText());
            ps.setString(3,jPasswordFieldSenha.getText());
            ps.setString(4, jTextFieldPerfil.getText());
            

            ps.executeUpdate();
            ps.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    public void update() {
        String SQL = "update usuarios set nome=?, login=?, senha=md5(?), perfil = ? "
                + "where id = ?";
        try {

            PreparedStatement ps = this.conexao.getConexao().prepareStatement(SQL);

            ps.setString(1, jTextFieldNome.getText());
            ps.setString(2, jTextFieldLogin.getText());
            ps.setString(3, jPasswordFieldSenha.getText());
            ps.setString(4, jTextFieldPerfil.getText());
            ps.setString(5, jTextFieldCodigo.getText());

            ps.executeUpdate();
            ps.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
